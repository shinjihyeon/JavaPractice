package pack_Conversion;

public class TempCalc {

	 int num = 1;
	 String txt = "df";
	
	public TempCalc() {} // 매개변수가 없는 생성자
						 // default 생성자
						 // 가재하지않더라도 자동으로 생성

	public TempCalc(int num, String txt) {
		this.num = num;
		this.txt = txt;
	}

	public TempCalc(int num) {
		this.num = num;
	}

	public TempCalc(String txt) {
		this.txt = txt;
	}

	
	
	
	
	
}
